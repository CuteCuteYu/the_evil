def get_quality_check_prompt(task_name: str) -> dict:
    """
    获取指定任务的质量检查提示词

    参数:
        task_name: 任务名称，如 "statistics", "personality" 等

    返回:
        包含 system 和 user 提示词的字典
    """
    return QUALITY_CHECK_PROMPTS.get(task_name, {})


def format_quality_check_prompt(task_name: str, **kwargs) -> str:
    """
    格式化指定任务的质量检查用户提示词

    参数:
        task_name: 任务名称
        **kwargs: 要填充的变量（如 original_content）

    返回:
        格式化后的用户提示词字符串
    """
    prompts = get_quality_check_prompt(task_name)
    if not prompts:
        return ""

    user_template = prompts.get("user", "")
    return user_template.format(**kwargs)
