def get_prompt(prompt_name):
    """
    获取指定名称的提示词配置

    参数:
        prompt_name: 提示词名称，如"statistics", "personality"等

    返回:
        包含system和user提示词的字典
    """
    return PROMPTS_CONFIG.get(prompt_name, {})


def format_prompt(prompt_name, **kwargs):
    """
    格式化指定名称的提示词，填充变量

    参数:
        prompt_name: 提示词名称
        **kwargs: 要填充的变量

    返回:
        格式化后的user提示词字符串
    """
    config = get_prompt(prompt_name)
    if not config:
        return ""

    user_template = config.get("user", "")
    return user_template.format(**kwargs)
